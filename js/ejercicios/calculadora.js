let numero1 = 10;
let numero2 = 5;

function restar(num1, num2) {
  return num1-num2;
}

function sumar(num1, num2) {
  return num1 + num2;
}

function dividir(num1, num2) {
  return num1/num2;
}

function multiplicar(num1, num2) {
  return num1*num2;
}

const resultadoRestar = restar(numero1, numero2);
console.log(resultadoRestar);

const resultadoSumar = sumar(numero1, numero2)
console.log(resultadoSumar);

const resultadoDividir = dividir(100, 2)
console.log(resultadoDividir);